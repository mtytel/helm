#include "b2Timer.h"

b2Timer::b2Timer()
{
    Reset();
}

void b2Timer::Reset()
{
	juceStartTime = juce::Time::getCurrentTime();
}

float32 b2Timer::GetMilliseconds() const
{
	return static_cast<float32> ((juce::Time::getCurrentTime() - juceStartTime).inMilliseconds());
}
